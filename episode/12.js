ついに「銀河猫にゃぼろん」上演の日を迎えた。<br>
シアターホール前で上演を待つ人の多さに尻込みする空太。<br>
その整理を生徒会長の館林に押し付け会場に向かう仁。<br>
関係者席に現れた千尋とゲームプロデユーサーの藤沢和希。<br>
舞台袖で一人最後まで練習を続ける七海。客入れが終了して、集合の号令をかける美咲。<br>
輪になった一同は全員で手を合わせ、最後の気合を入れる。<br>
