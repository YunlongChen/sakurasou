文化祭の出し物として「にゃぼろん」の制作を開始したさくら荘の面々。<br>
ディレクターとなった空太だが、絵コンテが全くはかどらない。<br>
数日後、徐々にスケジュールが遅れ始めているところに、生徒会から参加条件として企画内容をプレゼンするよう要求される。<br>
空太は、作業の遅れを取り戻すため、増員を決断し、現状のクオリティに見合う人材として、ましろがイギリスにいた頃のルームメイトであるリタを勧誘しようとするのだが…。<br>
