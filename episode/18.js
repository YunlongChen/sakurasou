和希とのタイトル審査会へ向けての打ち合わせを終えた空太。<br>
その後、七海と優子の合格発表を見に行こうとすると、今後のプランに関わるとましろもついてくることに。<br>
２人の仲の良さにむっとするましろをよそに学校に着くと、仁が受験の報告で来ていることを知る。<br>
仁と美咲がすれ違ったまま卒業して欲しくないという思いから、２人を引き合わせるべくそれぞれのもとに走る！ <br>
