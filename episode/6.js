ましろ当番になった七海は何もできないましろの世話を、バイトやさくら荘の食事当番、養成所の中間発表会の練習と一緒に完璧にこなし続ける。<br>
そんな七海のことを心配して仁に相談する空太だったが、ふとしたきっかけで仁の秘密を知ってしまうことに！<br>
<br>
美咲には黙っていて欲しいと言われるが、直後に当の美咲から仁の事で相談されてどぎまぎしてしまう。<br>
そうこうするうちに七海は中間発表会の当日を迎えるが、連日の無理がたたって体調を崩してしまう…。
